/**
 * Marker error for vendor (non-OpenAI-compat) provider failures whose message is
 * already operator-facing (an HTTP status+body or a `base_resp` assertion). The
 * execute handlers surface these as `provider_error`; anything else is treated
 * as a transport/`generation_failed` error.
 */
class MediaProviderError extends Error {}

/**
 * POST/GET JSON to a vendor endpoint and parse the body. A non-2xx becomes an
 * {@link ImageGenHttpError} (carrying status + body); a non-JSON 2xx body throws.
 * Faithful to the original `requestJson`.
 */
async function requestJson<T>(url: string, init: RequestInit): Promise<T> {
  const response = await fetch(url, init);
  const text = await response.text();
  if (!response.ok) throw new ImageGenHttpError(response.status, text);
  try {
    return JSON.parse(text) as T;
  } catch {
    throw new Error(`provider returned invalid JSON from ${url.split("?")[0]}`);
  }
}

/** Assert a Async `base_resp.status_code === 0`, else throw its status_msg. */
function assertAsyncOk(baseResp: AsyncBaseResp | undefined, label: string): void {
  const statusCode = baseResp?.status_code;
  if (typeof statusCode === "number" && statusCode !== 0) {
    throw new MediaProviderError(`${label} failed (${statusCode}): ${baseResp?.status_msg ?? "unknown error"}`);
  }
}

/** Decode a whitespace-tolerant hex string into bytes; reject malformed input. */
function bufferFromHex(value: string): Buffer {
  const normalized = value.replace(/\s+/g, "");
  if (!normalized || normalized.length % 2 !== 0 || /[^0-9a-f]/i.test(normalized)) {
    throw new Error("provider returned invalid hex audio data");
  }
  return Buffer.from(normalized, "hex");
}

