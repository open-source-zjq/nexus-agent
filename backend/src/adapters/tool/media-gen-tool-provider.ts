interface AsyncVideoRequest {
  prompt: string;
  duration?: number;
  resolution?: string;
  firstFrameImage?: ReferenceImage;
  lastFrameImage?: ReferenceImage;
  signal: AbortSignal;
  /** Poll deadline budget in ms; falls back to DEFAULT_TIMEOUT_MS when absent. */
  timeoutMs?: number;
  onUpdate?: ToolUpdate;
}

/**
 * Async media video pipeline (protocol: "async-video"). Faithful to
 * AsyncVideoClient: POST /v1/video_generation (first/last frame as data-URIs)
 * to obtain a `task_id`; emit a "submitted" onUpdate; then poll
 * /v1/query/video_generation?task_id= on a deadline, emitting each status; on
 * success read `file_id`, resolve its `download_url` via
 * /v1/files/retrieve?file_id=, and download the bytes. base_resp is asserted on
 * every JSON response.
 */
async function requestAsyncVideo(cfg: ResolvedConfig, request: AsyncVideoRequest): Promise<DecodedAudio> {
  const root = asyncRootUrl(cfg.endpoint);
  const headers = (): Record<string, string> => ({
    Authorization: `Bearer ${cfg.apiKey}`,
    "Content-Type": "application/json",
  });
  const createPayload = await requestJson<AsyncVideoResponse>(`${root}/v1/video_generation`, {
    method: "POST",
    headers: headers(),
    body: JSON.stringify({
      model: cfg.model,
      prompt: request.prompt,
      ...(request.duration !== undefined ? { duration: request.duration } : {}),
      ...(request.resolution ? { resolution: request.resolution } : {}),
      ...(request.firstFrameImage
        ? { first_frame_image: dataUri(request.firstFrameImage.mimeType, request.firstFrameImage.data) }
        : {}),
      ...(request.lastFrameImage
        ? { last_frame_image: dataUri(request.lastFrameImage.mimeType, request.lastFrameImage.data) }
        : {}),
    }),
    signal: request.signal,
  });
  assertAsyncOk(createPayload.base_resp, "Async video provider");
  const taskId = createPayload.task_id;
  if (!taskId) throw new Error("Async video provider returned no task_id");
  await emit(request.onUpdate, { status: "submitted", taskId, provider: "async-video" });

  const deadline = Date.now() + (request.timeoutMs ?? DEFAULT_TIMEOUT_MS);
  let lastStatus = "submitted";
  while (Date.now() < deadline) {
    await delay(DEFAULT_POLL_INTERVAL_MS, request.signal);
    const queryUrl = new URL(`${root}/v1/query/video_generation`);
    queryUrl.searchParams.set("task_id", taskId);
    const queryPayload = await requestJson<AsyncVideoResponse>(queryUrl.toString(), {
      method: "GET",
      headers: headers(),
      signal: request.signal,
    });
    assertAsyncOk(queryPayload.base_resp, "Async video provider");
    lastStatus = queryPayload.status || lastStatus;
    await emit(request.onUpdate, { status: lastStatus, taskId, provider: "async-video" });
    if (isFailureStatus(lastStatus)) {
      throw new Error(`Async video generation failed with status ${lastStatus}`);
    }
    if (!isSuccessStatus(lastStatus)) continue;
    const fileId = queryPayload.file_id;
    if (!fileId) throw new Error("Async video provider finished without file_id");
    const downloadUrl = await retrieveAsyncDownloadUrl(root, fileId, cfg, request.signal);
    const response = await fetch(downloadUrl, { method: "GET", signal: request.signal });
    if (!response.ok) throw new ImageGenHttpError(response.status, await response.text());
    const mimeType = response.headers.get("content-type")?.split(";")[0] || "video/mp4";
    return { data: Buffer.from(await response.arrayBuffer()), mimeType };
  }
  throw new Error(`Async video generation timed out after ${request.timeoutMs ?? DEFAULT_TIMEOUT_MS}ms (last status: ${lastStatus})`);
}

/** Resolve a Async file_id to its signed download_url via /v1/files/retrieve. */
async function retrieveAsyncDownloadUrl(
  root: string,
  fileId: string,
  cfg: ResolvedConfig,
  signal: AbortSignal,
): Promise<string> {
  const retrieveUrl = new URL(`${root}/v1/files/retrieve`);
  retrieveUrl.searchParams.set("file_id", fileId);
  const payload = await requestJson<AsyncVideoResponse>(retrieveUrl.toString(), {
    method: "GET",
    headers: { Authorization: `Bearer ${cfg.apiKey}`, "Content-Type": "application/json" },
    signal,
  });
  assertAsyncOk(payload.base_resp, "Async video provider");
  const downloadUrl = payload.file?.download_url;
  if (!downloadUrl) throw new Error("Async video provider returned no download_url");
  return downloadUrl;
}

/**
 * Strip a Async video route suffix (or trailing `/v1`) off a configured base so
 * the absolute /v1/video_generation, /v1/query/video_generation and
 * /v1/files/retrieve URLs can be composed from a common root. Faithful port.
 */
function asyncRootUrl(baseUrl: string): string {
  const normalized = trimTrailingSlashes(baseUrl.trim());
  if (!normalized) return "";
  for (const suffix of ["/v1/video_generation", "/video_generation", "/v1/query/video_generation"]) {
    if (normalized.toLowerCase().endsWith(suffix)) {
      return trimTrailingSlashes(normalized.slice(0, -suffix.length));
    }
  }
  if (normalized.toLowerCase().endsWith("/v1")) return trimTrailingSlashes(normalized.slice(0, -3));
  return normalized;
}

