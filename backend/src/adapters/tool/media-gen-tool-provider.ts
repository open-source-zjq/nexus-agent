/**
 * Provider-agnostic media/image generation tools.
 *
 * Ported from Nexus's image-gen / media-gen tool providers and reduced to a
 * single OpenAI-compatible HTTP shape per modality. Each modality is enabled by
 * configuring an endpoint + apiKey + model; when a modality is not configured
 * the tool returns a clear error result (it never throws) instructing the
 * operator to configure an endpoint. The provider as a whole is hidden from the
 * registry when `enabled` is false.
 *
 * Generated bytes are written to a hidden "<outputDir>/.nexus-media/" dir and
 * the absolute path + mimeType + byteSize are returned.
 */

const MEDIA_DIR = ".nexus-media";
const DEFAULT_TIMEOUT_MS = 120_000;
const DEFAULT_POLL_INTERVAL_MS = 5_000;

/** Image-to-image / image-to-video reference constants (ported from image-gen). */
const MAX_REFERENCE_IMAGE_BYTES = 10 * 1024 * 1024;
const REFERENCE_MIME_TYPES = new Set(["image/png", "image/jpeg", "image/webp"]);
const DEFAULT_MAX_REFERENCE_IMAGES = 4;
/** Aspect ratios accepted by generate_image; mapped to a WxH via {@link mapImageSize}. */
const ASPECT_RATIOS = new Set(["1:1", "4:3", "3:4", "16:9", "9:16", "3:2", "2:3", "21:9"]);
/** Long-edge pixel tiers selectable via image_size. */
const SIZE_TIERS: Record<string, number> = { "1K": 1024, "2K": 2048 };
const SIZE_STEP = 64;
const MIN_EDGE = 256;
/** Resolutions accepted by generate_video (ported from media-gen). */
const VIDEO_RESOLUTIONS = ["768P", "1080P"];
/** Audio container formats accepted by generate_speech / generate_music. */
const AUDIO_FORMATS = ["mp3", "wav", "flac", "pcm", "pcm16"] as const;

export interface MediaEndpointConfig {
  endpoint?: string;
  apiKey?: string;
  model?: string;
  /**
   * Optional vendor wire-shape selector (faithful to the original capabilities
   * `*GenerationProtocol`). Empty / omitted / unknown keeps the OpenAI-compatible
   * default path so existing configs are unaffected. Recognized values per
   * modality:
   *   - image:  "openai-images" | "async-image"
   *   - speech: "openai-speech" | "async-t2a" | "mimo-tts"
   *   - music:  "async-music"
   *   - video:  "async-video"
   */
  protocol?: string;
  /**
   * Optional default `WxH` size threaded into {@link mapImageSize} as the
   * fallback when neither aspect_ratio nor image_size is supplied (image
   * provider only). Mirrors the original image-gen `config.defaultSize`.
   */
  defaultSize?: string;
  /** Speech: default provider voice id/name used when the tool-call omits one. */
  voice?: string;
  /** Speech/Music: default audio container format used when the tool-call omits one. */
  format?: string;
  /** Video: default clip duration (seconds) used when the tool-call omits one. */
  defaultDuration?: number;
  /** Video: default resolution tier used when the tool-call omits one. */
  defaultResolution?: string;
  /** Per-modality request timeout budget in ms (falls back to DEFAULT_TIMEOUT_MS). */
  timeoutMs?: number;
}

/**
 * Minimal structural view of the attachment store used to create inline image
 * previews. The real {@link AttachmentStore} satisfies this via its `put`
 * method; when no store is supplied (the default) inline previews are skipped
 * with a warning, keeping the capability inert by default.
 */
export interface MediaAttachmentStore {
  put(input: {
    name: string;
    data: Buffer;
    mimeType?: string;
    threadId?: string;
    workspace?: string;
  }): Promise<{ id: string; name: string; mimeType: string; width?: number; height?: number }>;
}

export interface MediaProviderConfig {
  enabled?: boolean;
  image?: MediaEndpointConfig;
  speech?: MediaEndpointConfig;
  music?: MediaEndpointConfig;
  video?: MediaEndpointConfig;
  outputDir: string;
  /** Optional: maximum reference images accepted for image-to-image (default 4). */
  maxReferenceImages?: number;
  /** Optional: store for inline image previews. When absent, previews are skipped. */
  attachmentStore?: MediaAttachmentStore;
}

export function buildMediaToolProvider(config: MediaProviderConfig): ToolProvider {
  const enabled = Boolean(config.enabled);
  const outputDir = config.outputDir;
  const maxReferenceImages = config.maxReferenceImages ?? DEFAULT_MAX_REFERENCE_IMAGES;

  const tools: LocalTool[] = [
    buildImageTool(config.image, outputDir, maxReferenceImages, config.attachmentStore),
    buildSpeechTool(config.speech, outputDir),
    buildMusicTool(config.music, outputDir),
    buildVideoTool(config.video, outputDir),
  ];

  if (!enabled) {
    return {
      id: "media",
      kind: "media",
      enabled: false,
      available: false,
      reason: "media generation is disabled",
      tools: [],
    };
  }

  return {
    id: "media",
    kind: "media",
    enabled: true,
    available: true,
    tools,
  };
}

// --- image-generation SERVICE seam (write infographic, etc.) -----------------

/**
 * Result of a one-shot image generation via {@link ImageGenerationService}.
 * Mirrors {@link MediaFileResult} (a hidden `<outputDir>/.nexus-media/` file)
 * with the generated dimensions folded in when sniffable.
 */
export interface ImageGenerationResult {
  /** Path under the output dir (e.g. `.nexus-media/img-….png`). */
  relativePath: string;
  absolutePath: string;
  mimeType: string;
  byteSize: number;
  width?: number;
  height?: number;
  /** The size string actually sent to the provider (when one was resolved). */
  size?: string;
  model: string;
}

/**
 * A thin, service-callable image-generation seam over the SAME private HTTP +
 * file-write helpers the `generate_image` agent tool uses (`requestImageGenerate`
 * / `requestAsyncImage` → `writeMediaFile`). It exists so non-agent callers
 * (the Write surface's infographic action, T5.x) can rasterize a prompt without
 * going through the tool registry.
 *
 * `available` reflects the live image config (endpoint + apiKey + model present);
 * a route can gate a clean 503 on it. `generate` POSTs a text-to-image request
 * and writes the bytes to the hidden media dir, throwing a typed
 * {@link ImageGenerationError} (carrying a provider status when known) on
 * failure so the caller can surface a 400 rather than a 500.
 */
export interface ImageGenerationService {
  /** True when endpoint + apiKey + model are all configured. */
  readonly available: boolean;
  /** The configured model id (empty string when unavailable). */
  readonly model: string;
  generate(input: {
    prompt: string;
    /** Optional explicit `WxH`; falls back to the provider's configured defaultSize. */
    size?: string;
    signal?: AbortSignal;
  }): Promise<ImageGenerationResult>;
}

/** Typed image-generation failure carrying an optional upstream HTTP status. */
export class ImageGenerationError extends Error {
  constructor(
    message: string,
    /** Upstream provider HTTP status when the failure came from the provider. */
    readonly status?: number,
  ) {
    super(message);
    this.name = "ImageGenerationError";
  }
}

/**
 * Build an {@link ImageGenerationService} bound to an image media config and an
 * output dir. Reuses the module-private provider helpers, so the wire-shape,
 * `response_format` retry, URL-download fallback, Async branch, and the
 * `.nexus-media/` file layout are byte-identical to the `generate_image` tool.
 */
export function buildImageGenerationService(
  image: MediaEndpointConfig | undefined,
  outputDir: string,
): ImageGenerationService {
  const cfg = resolveConfig(image);
  return {
    available: cfg !== null,
    model: cfg?.model ?? "",
    async generate(input) {
      if (!cfg) {
        throw new ImageGenerationError(
          "image generation is not configured; set an endpoint, apiKey, and model for the image media provider",
        );
      }
      const prompt = pickString(input.prompt);
      if (!prompt) throw new ImageGenerationError("prompt is required");
      // Explicit size wins; otherwise fall back to the provider's defaultSize.
      const size = pickString(input.size) ?? mapImageSize(undefined, undefined, pickString(image?.defaultSize));
      const signal = withTimeout(input.signal ?? new AbortController().signal, resolveTimeoutMs(image));
      try {
        const generated =
          cfg.protocol === "async-image"
            ? await requestAsyncImage(cfg, { prompt, ...(size ? { size } : {}), signal })
            : await requestImageGenerate(cfg, { prompt, ...(size ? { size } : {}), signal });
        let mimeType = generated.mimeType;
        const detected = detectImageDimensions(generated.data);
        if (detected) mimeType = detected.mimeType;
        const file = await writeMediaFile(outputDir, "infographic", imageExtension(mimeType), generated.data, mimeType);
        return {
          relativePath: file.relativePath,
          absolutePath: file.absolutePath,
          mimeType,
          byteSize: file.byteSize,
          ...(detected?.width ? { width: detected.width } : {}),
          ...(detected?.height ? { height: detected.height } : {}),
          ...(size ? { size } : {}),
          model: cfg.model,
        };
      } catch (error) {
        if (error instanceof ImageGenHttpError) throw new ImageGenerationError(error.message, error.status);
        if (error instanceof MediaProviderError) throw new ImageGenerationError(error.message);
        throw new ImageGenerationError(networkErrorMessage(imagesUrl(cfg.endpoint, "generations"), error));
      }
    },
  };
}

// --- generate_image ---------------------------------------------------------

function buildImageTool(
  image: MediaEndpointConfig | undefined,
  outputDir: string,
  maxReferenceImages: number,
  attachmentStore: MediaAttachmentStore | undefined,
): LocalTool {
  return defineTool({
    name: "generate_image",
    description: [
      "Generate an image from a text prompt using the configured OpenAI-compatible images endpoint.",
      "Optionally pass reference_image_paths (image files inside the workspace) to guide the result (image-to-image).",
      "The generated image is written to a hidden media directory and its absolute path is returned.",
      "Generates exactly one image per call; call again for variations.",
    ].join(" "),
    toolKind: "tool_call",
    policy: "untrusted",
    inputSchema: {
      type: "object",
      properties: {
        prompt: { type: "string", description: "Detailed description of the image to generate" },
        size: { type: "string", description: "Optional WxH size, e.g. 1024x1024" },
        aspect_ratio: {
          type: "string",
          enum: [...ASPECT_RATIOS],
          description: "Optional aspect ratio; combined with image_size to derive a WxH size.",
        },
        image_size: {
          type: "string",
          enum: Object.keys(SIZE_TIERS),
          description: "Resolution tier, defaults to 1K",
        },
        reference_image_paths: {
          type: "array",
          items: { type: "string" },
          maxItems: maxReferenceImages,
          description: "Workspace-relative paths of reference images for image-to-image guidance",
        },
      },
      required: ["prompt"],
      additionalProperties: false,
    },
    async execute(args, context) {
      const startedAt = Date.now();
      const cfg = resolveConfig(image);
      if (!cfg) return notConfigured("image");
      const prompt = pickString(args.prompt);
      if (!prompt) return toolError("invalid_prompt", "prompt is required");
      // Explicit `size` wins; otherwise derive from aspect_ratio + image_size.
      const explicitSize = pickString(args.size);
      const aspectRatio = pickString(args.aspect_ratio);
      const imageSize = pickString(args.image_size);
      // Faithful to image-gen-tool-provider.js (~line 127): thread the provider's
      // configured default size as the mapImageSize fallback so a bare prompt
      // still sends a configured size.
      const defaultSize = pickString(image?.defaultSize);
      const size = explicitSize ?? mapImageSize(aspectRatio, imageSize, defaultSize);

      const references = await collectReferenceImages(
        args.reference_image_paths,
        context.workspace,
        maxReferenceImages,
      );
      if ("error" in references) return references.error;
      const endpoint: "edits" | "generations" =
        references.images.length > 0 ? "edits" : "generations";

      const signal = withTimeout(context.abortSignal, resolveTimeoutMs(image));
      try {
        const image_ =
          cfg.protocol === "async-image"
            ? await requestAsyncImage(cfg, {
                prompt,
                size,
                ...(endpoint === "edits" ? { images: references.images } : {}),
                signal,
              })
            : endpoint === "edits"
              ? await requestImageEdit(cfg, { prompt, size, images: references.images, signal })
              : await requestImageGenerate(cfg, { prompt, size, signal });
        let mimeType = image_.mimeType;
        const detected = detectImageDimensions(image_.data);
        if (detected) mimeType = detected.mimeType;
        const file = await writeMediaFile(outputDir, "img", imageExtension(mimeType), image_.data, mimeType);

        // Inline preview: best-effort; never fails the generation.
        const warnings: string[] = [];
        const attachments: Array<Record<string, unknown>> = [];
        if (attachmentStore) {
          try {
            const attachment = await attachmentStore.put({
              name: file.absolutePath.split("/").pop() || "image.png",
              data: image_.data,
              mimeType,
              threadId: context.threadId,
              workspace: context.workspace,
            });
            attachments.push({
              id: attachment.id,
              name: attachment.name,
              mimeType: attachment.mimeType,
              ...(attachment.width ? { width: attachment.width } : {}),
              ...(attachment.height ? { height: attachment.height } : {}),
            });
          } catch (error) {
            warnings.push(`inline preview unavailable: ${errorMessageMedia(error)}`);
          }
        } else {
          warnings.push("inline preview unavailable: attachment store is disabled");
        }

        const fileEntry = {
          ...file,
          ...(detected?.width ? { width: detected.width } : {}),
          ...(detected?.height ? { height: detected.height } : {}),
        };
        return {
          output: {
            files: [fileEntry],
            attachments,
            model: cfg.model,
            ...(size ? { size } : {}),
            endpoint,
            warnings,
            telemetry: mediaTelemetry(startedAt),
          },
        };
      } catch (error) {
        if (error instanceof ImageGenHttpError) {
          if (endpoint === "edits" && (error.status === 404 || error.status === 405 || error.status === 501)) {
            return toolError(
              "edits_unsupported",
              "the configured image provider does not support reference images (/images/edits); retry generate_image without reference_image_paths",
            );
          }
          return toolError("provider_error", error.message);
        }
        if (error instanceof MediaProviderError) return toolError("provider_error", error.message);
        return toolError("generation_failed", networkErrorMessage(imagesUrl(cfg.endpoint, endpoint), error));
      }
    },
  });
}

interface GeneratedImage {
  data: Buffer;
  mimeType: string;
}

/** HTTP error carrying status + body so edits-unsupported downgrades can match on it. */
class ImageGenHttpError extends Error {
  constructor(
    readonly status: number,
    readonly body: string,
  ) {
    super(`HTTP ${status}: ${body}`);
  }
}

/**
 * POST a JSON text-to-image generation. Faithful to the original: providers
 * that reject `response_format` get one retry without it, and providers that
 * return a URL instead of b64_json get a second download.
 */
async function requestImageGenerate(
  cfg: ResolvedConfig,
  request: { prompt: string; size?: string; signal: AbortSignal },
): Promise<GeneratedImage> {
  const url = imagesUrl(cfg.endpoint, "generations");
  const body = (includeResponseFormat: boolean): string =>
    JSON.stringify({
      model: cfg.model,
      prompt: request.prompt,
      n: 1,
      ...(request.size ? { size: request.size } : {}),
      ...(includeResponseFormat ? { response_format: "b64_json" } : {}),
    });
  return requestImage(
    url,
    (includeResponseFormat) => ({
      headers: { Authorization: `Bearer ${cfg.apiKey}`, "Content-Type": "application/json" },
      body: body(includeResponseFormat),
    }),
    request.signal,
  );
}

/** POST a multipart image-to-image edit with reference image(s). */
async function requestImageEdit(
  cfg: ResolvedConfig,
  request: { prompt: string; size?: string; images: ReferenceImage[]; signal: AbortSignal },
): Promise<GeneratedImage> {
  const url = imagesUrl(cfg.endpoint, "edits");
  const buildForm = (includeResponseFormat: boolean): FormData => {
    const form = new FormData();
    form.set("model", cfg.model);
    form.set("prompt", request.prompt);
    if (request.size) form.set("size", request.size);
    if (includeResponseFormat) form.set("response_format", "b64_json");
    const field = request.images.length > 1 ? "image[]" : "image";
    for (const ref of request.images) {
      form.append(field, new Blob([new Uint8Array(ref.data)], { type: ref.mimeType }), ref.name);
    }
    return form;
  };
  return requestImage(
    url,
    (includeResponseFormat) => ({
      headers: { Authorization: `Bearer ${cfg.apiKey}` },
      body: buildForm(includeResponseFormat),
    }),
    request.signal,
  );
}

/**
 * Shared image POST with two compat fallbacks: a 4xx whose body mentions
 * `response_format` is retried once without it; a payload that returns a `url`
 * instead of `b64_json` is downloaded.
 */
async function requestImage(
  url: string,
  init: (includeResponseFormat: boolean) => { headers: Record<string, string>; body: string | FormData },
  signal: AbortSignal,
): Promise<GeneratedImage> {
  const post = (includeResponseFormat: boolean): Promise<Response> =>
    fetch(url, { method: "POST", ...init(includeResponseFormat), signal });

  let response = await post(true);
  if (!response.ok && response.status >= 400 && response.status < 500) {
    const errorBody = await response.text();
    if (!/response_format/i.test(errorBody)) throw new ImageGenHttpError(response.status, errorBody);
    response = await post(false);
  }
  if (!response.ok) throw new ImageGenHttpError(response.status, await response.text());

  const payload = (await response.json()) as ImagesResponse;
  const entry = payload.data?.[0];
  if (entry?.b64_json) {
    return { data: Buffer.from(entry.b64_json, "base64"), mimeType: "image/png" };
  }
  if (entry?.url) {
    const download = await fetch(entry.url, { signal });
    if (!download.ok) throw new ImageGenHttpError(download.status, await download.text());
    const mimeType = download.headers.get("content-type")?.split(";")[0] || "image/png";
    return { data: Buffer.from(await download.arrayBuffer()), mimeType };
  }
  throw new Error("image provider returned no image data");
}

// --- Async image (protocol: "async-image") ------------------------------

/**
 * Async /v1/image_generation adapter. Faithful to the original
 * AsyncImageClient: a single JSON POST carrying `prompt_optimizer` +
 * `response_format: "base64"`; reference images become a `subject_reference`
 * array of character data-URIs; the response is asserted via `base_resp` and the
 * image bytes come from `data.image_base64[0]` (base64) or a downloaded
 * `data.image_urls[0]`. Dimension fields are derived per-model by
 * {@link asyncImageDimensionFields}.
 */
async function requestAsyncImage(
  cfg: ResolvedConfig,
  request: { prompt: string; size?: string; images?: ReferenceImage[]; signal: AbortSignal },
): Promise<GeneratedImage> {
  const url = asyncImageGenerationUrl(cfg.endpoint);
  const body: Record<string, unknown> = {
    model: cfg.model,
    prompt: request.prompt,
    ...asyncImageDimensionFields(cfg.model, request.size),
    prompt_optimizer: true,
    response_format: "base64",
    n: 1,
  };
  if (request.images && request.images.length > 0) {
    body.subject_reference = request.images.map((image) => ({
      type: "character",
      image_file: `data:${image.mimeType};base64,${image.data.toString("base64")}`,
    }));
  }
  let response: Response;
  try {
    response = await fetch(url, {
      method: "POST",
      headers: { Authorization: `Bearer ${cfg.apiKey}`, "Content-Type": "application/json" },
      body: JSON.stringify(body),
      signal: request.signal,
    });
  } catch (error) {
    throw new Error(networkErrorMessage(url, error));
  }
  const text = await response.text();
  if (!response.ok) throw new ImageGenHttpError(response.status, text);
  let payload: AsyncImageResponse;
  try {
    payload = JSON.parse(text) as AsyncImageResponse;
  } catch {
    throw new Error("Async image provider returned invalid JSON");
  }
  assertAsyncOk(payload.base_resp, "Async image provider");
  const b64 = payload.data?.image_base64?.[0];
  if (b64) {
    return { data: Buffer.from(b64, "base64"), mimeType: "image/jpeg" };
  }
  const imageUrl = payload.data?.image_urls?.[0];
  if (imageUrl) {
    const download = await fetch(imageUrl, { signal: request.signal });
    if (!download.ok) throw new ImageGenHttpError(download.status, await download.text());
    const mimeType = download.headers.get("content-type")?.split(";")[0] || "image/jpeg";
    return { data: Buffer.from(await download.arrayBuffer()), mimeType };
  }
  throw new Error("Async image provider returned no image data");
}

/** Resolve the Async /v1/image_generation URL, tolerating bare/`/v1` bases. */
function asyncImageGenerationUrl(baseUrl: string): string {
  const normalized = trimTrailingSlashes(baseUrl.trim());
  const lower = normalized.toLowerCase();
  if (!normalized) return "/v1/image_generation";
  if (lower.endsWith("/v1/image_generation") || lower.endsWith("/image_generation")) return normalized;
  if (lower.endsWith("/v1")) return `${normalized}/image_generation`;
  return `${normalized}/v1/image_generation`;
}

/** Async aspect-ratio table used to snap a WxH onto the nearest label. */
const ASYNC_ASPECT_RATIOS: Array<{ label: string; value: number }> = [
  { label: "1:1", value: 1 },
  { label: "16:9", value: 16 / 9 },
  { label: "4:3", value: 4 / 3 },
  { label: "3:2", value: 3 / 2 },
  { label: "2:3", value: 2 / 3 },
  { label: "3:4", value: 3 / 4 },
  { label: "9:16", value: 9 / 16 },
];

/**
 * Derive Async dimension fields from a `WxH` size string. `image-01` accepts
 * explicit `{ width, height }`; other models accept the nearest
 * `{ aspect_ratio }` label (log-distance match). Faithful port.
 */
function asyncImageDimensionFields(model: string, size: string | undefined): Record<string, unknown> {
  const match = size?.trim().match(/^(\d+)x(\d+)$/);
  if (!match) return {};
  const width = Number(match[1]);
  const height = Number(match[2]);
  if (!Number.isFinite(width) || !Number.isFinite(height) || width <= 0 || height <= 0) return {};
  if (model.trim() === "image-01") return { width, height };
  const target = width / height;
  let best = ASYNC_ASPECT_RATIOS[0];
  let bestDiff = Number.POSITIVE_INFINITY;
  for (const candidate of ASYNC_ASPECT_RATIOS) {
    const diff = Math.abs(Math.log(candidate.value / target));
    if (diff < bestDiff) {
      bestDiff = diff;
      best = candidate;
    }
  }
  return { aspect_ratio: best.label };
}

