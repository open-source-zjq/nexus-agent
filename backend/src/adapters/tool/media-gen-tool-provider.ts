function networkErrorMessage(url: string, error: unknown): string {
  const target = url.split("?")[0];
  if (error instanceof DOMException && error.name === "TimeoutError") {
    return `media request to ${target} timed out`;
  }
  if (error instanceof DOMException && error.name === "AbortError") {
    return `media request to ${target} was canceled`;
  }
  return `media request to ${target} failed: ${error instanceof Error ? error.message : String(error)}`;
}

