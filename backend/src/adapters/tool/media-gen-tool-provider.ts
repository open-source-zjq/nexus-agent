function buildSpeechTool(speech: MediaEndpointConfig | undefined, outputDir: string): LocalTool {
  return defineTool({
    name: "generate_speech",
    description: [
      "Generate spoken audio from text using the configured text-to-speech provider.",
      "The generated audio is written to a hidden media directory and returned as a generated file.",
      "Use voice for a provider voice id/name and style for voice-style instructions when needed.",
    ].join(" "),
    toolKind: "tool_call",
    policy: "untrusted",
    inputSchema: {
      type: "object",
      properties: {
        text: { type: "string", description: "Text to synthesize into speech" },
        voice: { type: "string", description: "Optional provider voice id/name" },
        style: { type: "string", description: "Optional voice style instruction for providers that support it" },
        format: { type: "string", enum: [...AUDIO_FORMATS] },
      },
      required: ["text"],
      additionalProperties: false,
    },
    async execute(args, context) {
      const startedAt = Date.now();
      const cfg = resolveConfig(speech);
      if (!cfg) return notConfigured("speech");
      const text = pickString(args.text);
      if (!text) return toolError("invalid_text", "text is required");
      // Format falls back to the configured default, then mp3.
      const format = normalizeAudioFormat(pickString(args.format) ?? pickString(speech?.format));
      // Vendor protocols accept an optional voice; OpenAI-compat falls back to
      // "alloy" only when none is supplied (faithful to OpenAiCompatSpeechClient).
      // A configured default voice is used when the tool-call omits one.
      const voiceArg = pickString(args.voice) ?? pickString(speech?.voice);
      const style = pickString(args.style);
      const signal = withTimeout(context.abortSignal, resolveTimeoutMs(speech));
      try {
        const media =
          cfg.protocol === "async-t2a"
            ? await requestAsyncSpeech(cfg, { text, voice: voiceArg, format, signal })
            : cfg.protocol === "mimo-tts"
              ? await requestMimoSpeech(cfg, { text, voice: voiceArg, style, format, signal })
              : await requestOpenAiSpeech(cfg, { text, voice: voiceArg, style, format, signal });
        const file = await writeMediaFile(outputDir, "speech", audioExtension(format), media.data, media.mimeType);
        return {
          output: {
            files: [file],
            model: cfg.model,
            ...(voiceArg ? { voice: voiceArg } : {}),
            format,
            telemetry: mediaTelemetry(startedAt),
          },
        };
      } catch (error) {
        if (error instanceof MediaProviderError) return toolError("provider_error", error.message);
        return toolError("generation_failed", networkErrorMessage(apiUrl(cfg.endpoint, "/v1/audio/speech"), error));
      }
    },
  });
}

